/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    bool isPalindrome(ListNode* head) {
        stack<int> s;
        ListNode *dummy = head;
        while (dummy) {
            s.push(dummy->val);
            dummy = dummy->next;
        }
        while (head) {
            if (head->val != s.top()) return false;
            head = head->next;
            s.pop();
        }
        return true;
    }
};
